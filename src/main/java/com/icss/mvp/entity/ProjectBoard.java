package com.icss.mvp.entity;

public class ProjectBoard {
	
	private int red;
	
	private int green;
	
	private int yellow;

	public int getRed() {
		return red;
	}

	public void setRed(int red) {
		this.red = red;
	}

	public int getGreen() {
		return green;
	}

	public void setGreen(int green) {
		this.green = green;
	}

	public int getYellow() {
		return yellow;
	}

	public void setYellow(int yellow) {
		this.yellow = yellow;
	}
	
	

}
