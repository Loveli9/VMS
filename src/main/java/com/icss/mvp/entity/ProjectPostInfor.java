package com.icss.mvp.entity;

public class ProjectPostInfor {
	
	private String post;
	
	private String rank;
	
	private Integer budget;
	
	private Integer actual;
	
	private String keyRole;
	
	private String roleOrPost;

	public String getPost() {
		return post;
	}

	public void setPost(String post) {
		this.post = post;
	}

	public String getRank() {
		return rank;
	}

	public void setRank(String rank) {
		this.rank = rank;
	}

	public Integer getBudget() {
		return budget;
	}

	public void setBudget(Integer budget) {
		this.budget = budget;
	}

	public Integer getActual() {
		return actual;
	}

	public void setActual(Integer actual) {
		this.actual = actual;
	}

	public String getKeyRole() {
		return keyRole;
	}

	public void setKeyRole(String keyRole) {
		this.keyRole = keyRole;
	}

	public String getRoleOrPost() {
		return roleOrPost;
	}

	public void setRoleOrPost(String roleOrPost) {
		this.roleOrPost = roleOrPost;
	}
	
}
