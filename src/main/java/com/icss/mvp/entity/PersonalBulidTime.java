package com.icss.mvp.entity;

public class PersonalBulidTime {
	private String proId;
	private String pbiId;
	private String pbiName;
	private String startTime;
	private String endTime;
	private String bulidType;
	private String bulidResult;

	public String getProId() {
		return proId;
	}

	public void setProId(String proId) {
		this.proId = proId;
	}

	public String getPbiId() {
		return pbiId;
	}

	public void setPbiId(String pbiId) {
		this.pbiId = pbiId;
	}

	public String getPbiName() {
		return pbiName;
	}

	public void setPbiName(String pbiName) {
		this.pbiName = pbiName;
	}

	public String getStartTime() {
		return startTime;
	}

	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}

	public String getEndTime() {
		return endTime;
	}

	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}

	public String getBulidType() {
		return bulidType;
	}

	public void setBulidType(String bulidType) {
		this.bulidType = bulidType;
	}

	public String getBulidResult() {
		return bulidResult;
	}

	public void setBulidResult(String bulidResult) {
		this.bulidResult = bulidResult;
	}
}